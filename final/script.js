const convertTitleName = function (title) {
  const exceptions = ['the', 'a', 'an'];

  const NewTitle = title
    .toLowerCase()
    .split(' ')
    .map(el =>
      exceptions.includes(el) ? el : el[0].toUpperCase() + el.slice(1)
    )
    .join(' ');
  return NewTitle;
};

